// Considere a variável balance, que representa a quantia em conta da pessoa cliente do Nicoland e escreva quatro funções que:

// Adiciona um valor ao balance; +
const balance = 100;

function sumBalance(value) {
  return value + balance;
}
// Subtraia um valor do balance; -
function sumBalance(value) {
  return balance - value;
}

// Multiplique o valor do balance por uma taxa; *
function sumBalance(value) {
  return balance * value;
}

// Divida o valor do balance. /
function sumBalance(value) {
  return balance / value;
}

console.log(sumBalance(300))
console.log(sumBalance(50))
console.log(sumBalance(2))
console.log(sumBalance(5))

